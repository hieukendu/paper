from __future__ import annotations

"""Generate LaTeX experiment tables directly from the verified result JSON."""

import json
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
TABLES = ROOT / "manuscript" / "latex" / "tables"

DISPLAY_NAMES = {
    "vipragsent_full": "ViPragSent",
    "phobert_finetune": "PhoBERT fine-tune",
    "visobert_finetune": "ViSoBERT fine-tune",
    "vipragsent_no_rationale": "ViPragSent without rationale",
    "vipragsent_no_emotion": "ViPragSent without emotion",
    "vipragsent_no_polarity": "ViPragSent without polarity",
    "vipragsent_no_uncertainty": "ViPragSent without uncertainty",
}


def load(name: str) -> dict:
    return json.loads((ROOT / "results" / name).read_text(encoding="utf-8"))


def cell(metric: dict) -> str:
    ci = metric["ci95"]
    return f"{metric['mean']:.2f} [{ci[0]:.2f}, {ci[1]:.2f}]"


def display_name(system_id: str) -> str:
    return DISPLAY_NAMES.get(system_id, system_id.replace("_", "\\\\_"))


def main() -> None:
    main_result = load("main_pragmatic.json")
    visobert = load("p0_visobert_baseline.json")
    ablation = load("p0_multi_seed_ablation.json")
    p1 = load("p1_source_stratified_sensitivity.json")
    p2 = load("p2_multi_seed_low_resource.json")

    main_lines = [
        "% Generated by scripts/22_generate_manuscript_experiment_tables.py from verified result JSON.",
        "\\small",
        "\\setlength{\\tabcolsep}{3pt}",
        "\\begin{tabular}{lccccccc}",
        "\\toprule",
        "System & Implicit & Sarcasm & Irony & Idiom/fig. & Code-switch. & Mocking & Macro \\\\",
        "\\midrule",
    ]
    for system_id, system in main_result["systems"].items():
        metrics = system["metrics"]
        label = system.get("label", system_id).replace("_", "\\_")
        values = [metrics[name]["mean"] for name in ("implicit_sentiment", "sarcasm", "irony", "idiom_figurative", "code_switching", "mocking", "macro_pragmatic_f1")]
        main_lines.append(label + " & " + " & ".join(f"{value:.2f}" for value in values) + " \\\\")
    metrics = visobert["system"]["aggregate"]
    # The main table keeps point estimates compact; intervals are reported in the caption/text.
    names = ("implicit_sentiment", "sarcasm", "irony", "idiom_figurative", "code_switching", "mocking")
    values = [metrics[name]["mean"] for name in names]
    values.append(metrics["macro_pragmatic_f1"]["mean"])
    main_lines.extend(["ViSoBERT fine-tune & " + " & ".join(f"{value:.2f}" for value in values) + " \\\\", "\\bottomrule", "\\end{tabular}%", ""])
    (TABLES / "table_main_pragmatic.tex").write_text("\n".join(main_lines), encoding="utf-8")

    ablation_lines = [
        "% Generated by scripts/22_generate_manuscript_experiment_tables.py from verified result JSON.",
        "\\small",
        "\\begin{tabular}{lr}",
        "\\toprule",
        "Configuration & Macro pragmatic F1 (three seeds) \\\\",
        "\\midrule",
    ]
    for system_id, payload in ablation["systems"].items():
        system_id = display_name(system_id)
        metric = payload["aggregate"]["macro_pragmatic_f1"]
        ablation_lines.append(f"{system_id.replace('_', '\\_')} & {cell(metric)} \\\\")
    ablation_lines.extend(["\\bottomrule", "\\end{tabular}%", ""])
    (TABLES / "table_ablation_ordinary.tex").write_text("\n".join(ablation_lines), encoding="utf-8")

    analysis_lines = [
        "% Generated by scripts/22_generate_manuscript_experiment_tables.py from verified result JSON.",
        "\\small",
        "\\resizebox{\\columnwidth}{!}{%",
        "\\begin{tabular}{lcc}",
        "\\toprule",
        "System & ViSoBERT-local & VIVID-labelled \\\\",
        "\\midrule",
    ]
    for system_id in ("vipragsent_full", "phobert_finetune", "visobert_finetune"):
        payload = p1["systems"][system_id]
        system_id = display_name(system_id)
        local = cell(payload["visobert_local"]["aggregate"]["macro_pragmatic_f1"])
        vivid = cell(payload["VIVID_seed_and_irony_generation"]["aggregate"]["macro_pragmatic_f1"])
        analysis_lines.append(f"{system_id.replace('_', '\\_')} & {local} & {vivid} \\\\")
    analysis_lines.extend(["\\midrule", "\\multicolumn{3}{l}{\\textit{P2: sarcasm macro F1 (three seeds)}} \\\\", "Budget & PhoBERT & ViPragSent \\\\", "\\midrule"])
    for budget, systems in p2["budgets"].items():
        pho = cell(systems["phobert_finetune"]["aggregate"]["sarcasm"])
        full = cell(systems["vipragsent_full"]["aggregate"]["sarcasm"])
        analysis_lines.append(f"{budget} & {pho} & {full} \\\\")
    analysis_lines.extend(["\\bottomrule", "\\end{tabular}%", "}", ""])
    (TABLES / "table_source_lowresource.tex").write_text("\n".join(analysis_lines), encoding="utf-8")

    external_lines = [
        "% Generated by scripts/22_generate_manuscript_experiment_tables.py from verified result JSON.",
        "\\small",
        "\\resizebox{\\textwidth}{!}{%",
        "\\begin{tabular}{lccc}",
        "\\toprule",
        "System & UIT-VSFC polarity & UIT-VSMEC emotion & AIVIVN-2019 polarity \\\\",
        "\\midrule",
    ]
    rows = [(name, payload) for name, payload in ablation["external_benchmarks"].items()]
    rows.append(("visobert_finetune", visobert["external_benchmarks"]))
    for system_id, benchmarks in rows:
        system_id = display_name(system_id)
        values = []
        for dataset in ("uit_vsfc", "uit_vsmec", "aivivn_2019"):
            aggregate = benchmarks[dataset]["aggregate"]
            metric = next(iter(aggregate.values()))
            values.append(cell(metric))
        external_lines.append(f"{system_id.replace('_', '\\_')} & " + " & ".join(values) + " \\\\")
    external_lines.extend(["\\bottomrule", "\\end{tabular}%", "}", ""])
    (TABLES / "table_external_diagnostics.tex").write_text("\n".join(external_lines), encoding="utf-8")


if __name__ == "__main__":
    main()
